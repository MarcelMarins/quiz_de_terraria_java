    public void dados() {
        try {
            Conexao.Conectar();
            Conexao.ps = Conexao.con.prepareStatement("SELECT * FROM pergunta WHERE per_niv_id = " + frmMenu.dificuldade);
            Conexao.rs = Conexao.ps.executeQuery();
            if (Conexao.rs.next()) {
                if (i < 5) {
                    pergunta = Conexao.rs.getString("per_perg" + i);
                    perg.setText(pergunta);
                    Conexao.ps = Conexao.con.prepareStatement("SELECT * FROM resposta WHERE res_niv_id = " + frmMenu.dificuldade + " AND res_num = " + i);
                    Conexao.rs = Conexao.ps.executeQuery();
                    if (Conexao.rs.next()) {
                        alt1.setText(Conexao.rs.getString("res_resp1"));
                        alt2.setText(Conexao.rs.getString("res_resp2"));
                        alt3.setText(Conexao.rs.getString("res_resp3"));
                        alt4.setText(Conexao.rs.getString("res_resp4"));
                    }
                } else {
                    this.setVisible(false);
                    if (JOptionPane.showConfirmDialog(null, "Parabéns!\nVocê concluiu o quiz!\nDeseja jogar novamente?", "Acabou!", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                        i = 1;
                        chances = 3;
                        ch.setText("" + chances);
                        dados();
                        this.setVisible(true);
                    } else {
                        if (JOptionPane.showConfirmDialog(null, "Deseja voltar para o menu?", "Voltar para o menu", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                            frmMenu x = new frmMenu();
                            x.setVisible(true);
                        } else {
                            i = 1;
                            chances = 3;
                            ch.setText("" + chances);
                            dados();
                            this.setVisible(true);
                        }
                    }
                    ch.setText("" + chances);
                    i = 1;
                    chances = 3;
                    dados();
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

