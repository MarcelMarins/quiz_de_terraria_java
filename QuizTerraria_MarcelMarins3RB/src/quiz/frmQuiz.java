    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (alt1.isSelected() || alt2.isSelected() || alt3.isSelected() || alt4.isSelected()) {
            try {
                Conexao.Conectar();
                Conexao.ps = Conexao.con.prepareStatement("SELECT * FROM resposta WHERE res_niv_id = " + frmMenu.dificuldade + " AND res_num = " + i);
                Conexao.rs = Conexao.ps.executeQuery();
                if (Conexao.rs.next()) {
                    if (alternativa.equals(Conexao.rs.getString("res_rescorreta"))) {
                        if (frmMenu.dificuldade == 1) {
                            try {
                                Conexao.Conectar();
                                Conexao.ps = Conexao.con.prepareStatement("UPDATE jogador SET jog_acfacil = jog_acfacil + 1, jog_acertos = jog_acertos + 1  WHERE jog_nickname = '" + frmLogin.nick + "'");
                                Conexao.ps.executeUpdate();
                            } catch (SQLException ex) {
                                JOptionPane.showMessageDialog(null, ex);
                            }

                        } else if (frmMenu.dificuldade == 2) {
                            try {
                                Conexao.Conectar();
                                Conexao.ps = Conexao.con.prepareStatement("UPDATE jogador SET jog_acmedio = jog_acmedio + 1, jog_acertos = jog_acertos + 1  WHERE jog_nickname = '" + frmLogin.nick + "'");
                                Conexao.ps.executeUpdate();
                            } catch (SQLException ex) {
                                JOptionPane.showMessageDialog(null, ex);
                            }
                        } else if (frmMenu.dificuldade == 3) {
                            try {
                                Conexao.Conectar();
                                Conexao.ps = Conexao.con.prepareStatement("UPDATE jogador SET jog_acdificil = jog_acdificil + 1, jog_acertos = jog_acertos + 1  WHERE jog_nickname = '" + frmLogin.nick + "'");
                                Conexao.ps.executeUpdate();
                            } catch (SQLException ex) {
                                JOptionPane.showMessageDialog(null, ex);
                            }
                        }

                        JOptionPane.showMessageDialog(null, "Parabens, você acertou!");
                        i++;
                        dados();
                    } else {
                        if (chances <= 1) {
                            this.setVisible(false);
                            if (JOptionPane.showConfirmDialog(null, "Suas chances acabaram :(\nA alternativa correta era:\n" + Conexao.rs.getString("res_rescorreta") + "\nDeseja tentar novamente!", "Suas chances acabaram! ;-;", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                                i = 1;
                                chances = 3;
                                ch.setText("" + chances);
                                dados();
                                this.setVisible(true);
                            } else {
                                if (JOptionPane.showConfirmDialog(null, "Deseja voltar para o menu?", "Voltar para o menu", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                                    frmMenu x = new frmMenu();
                                    x.setVisible(true);
                                } else {
                                    i = 1;
                                    chances = 3;
                                    ch.setText("" + chances);
                                    dados();
                                    this.setVisible(true);
                                }
                            }
                        } else {
                            chances--;
                            ch.setText("" + chances);
                            JOptionPane.showMessageDialog(null, "Alternativa errada, você tem mais " + chances + " chances de acertar");
                            try {
                                Conexao.Conectar();
                                Conexao.ps = Conexao.con.prepareStatement("UPDATE jogador SET jog_erros = jog_erros + 1 WHERE jog_nickname = '" + frmLogin.nick + "'");
                                Conexao.ps.executeUpdate();

                            } catch (SQLException ex) {
                                JOptionPane.showMessageDialog(null, ex);
                            }
                        }
                    }

                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma resposta!");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

